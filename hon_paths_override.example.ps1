# Optional custom install paths for non-standard HoN/Juvio location.
# Rename/copy this file to: hon_paths_override.ps1
# Keep quotes and use full absolute paths.
# Use this only if your install path is not standard.

# Example:
# $HoNDocsRoot = "D:\Juvio\Heroes of Newerth"
# $HoNLocalRoot = "D:\Juvio\heroes of newerth"
# $HoNArchivePath = "D:\Juvio\heroes of newerth\resources0.jz"
#
# What each value means:
# $HoNDocsRoot    -> folder that contains startup.cfg
# $HoNLocalRoot   -> folder that contains resources0.jz
# $HoNArchivePath -> full path to resources0.jz
