# shorten_specific_abilities.py
# Shortens specific overflowing ability descriptions.
# ENCODING RULES: entities_en.str MUST keep BOM (EF BB BF). Byte replacement only.

import sys
sys.stdout.reconfigure(encoding='utf-8')

path = r'd:\HoN_RU_Pack\bundle\entities_en.str'

with open(path, 'rb') as f:
    data = f.read()

bom = b'\xef\xbb\xbf'
assert data.startswith(bom), "BOM missing! Abort."

total = 0

def fix(old_str, new_str):
    global data, total
    old = old_str.encode('utf-8')
    new = new_str.encode('utf-8')
    count = data.count(old)
    if count:
        data = data.replace(old, new)
        total += count
        print(f"  [{count}x] fixed: {old_str[:60]!r}...")
    else:
        print(f"  [SKIP] not found: {old_str[:60]!r}")

