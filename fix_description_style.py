# Process all lines
for i, line in enumerate(lines):
    if '\t' not in line: continue
    key = line.split('\t')[0].strip()
    if ':' in key: continue
    if '_description_simple' not in key and '_description\"' not in key and not key.endswith('_description'):
        # Also fix item descriptions
        if 'Item_' not in key: continue

    original = line
    tab_prefix = line.split('\t',1)[0]
    val = line.split('\t',1)[1] if '\t' in line else ''

    for (key_pat, old, new, *_) in PHRASE_REPLACEMENTS:
        if old in val:
            val = val.replace(old, new)

    if val != (line.split('\t',1)[1] if '\t' in line else ''):
        lines[i] = tab_prefix + '\t' + val
        fixes_total += 1

print(f'=== Total text style fixes: {fixes_total} lines modified ===')
new_text = '\r\n'.join(lines)
with open(path,'wb') as f:
    f.write(bom + new_text.encode('utf-8'))
